public class Switch {
    public static void main(String[] args) {

        int number = 4;
        switch (number) {
            case 1:
                System.out.println("Dosa");
                break;
            case 2:
                System.out.println("Samosa");
                break;
            case 3:
                System.out.println("Shakes");
                break;
            default:
                System.out.println("We realise we are dreaming");
        }
    }

}
